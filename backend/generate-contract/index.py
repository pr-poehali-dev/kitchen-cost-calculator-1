def handler(event: dict, context) -> dict:
    """Генерация договора бытового подряда на изготовление мебели в формате DOCX.
    Принимает данные клиента, возвращает base64-закодированный DOCX-файл или URL из S3."""
    if event.get('httpMethod') == 'OPTIONS':
        return {
            'statusCode': 200,
            'headers': {
                'Access-Control-Allow-Origin': '*',
                'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
                'Access-Control-Allow-Headers': 'Content-Type, X-User-Id, X-Auth-Token, X-Session-Id',
                'Access-Control-Max-Age': '86400'
            },
            'body': ''
        }

    try:
        body = json.loads(event.get('body') or '{}')
    except Exception:
        body = {}

    data = body.get('data', body)

    docx_bytes = generate_contract_docx(data)

    # Загружаем в S3 и возвращаем ссылку
    file_key = f'contracts/contract_{data.get("contract_number", uuid.uuid4().hex)}_{uuid.uuid4().hex[:6]}.docx'

    s3 = boto3.client(
        's3',
        endpoint_url='https://bucket.poehali.dev',
        aws_access_key_id=os.environ['AWS_ACCESS_KEY_ID'],
        aws_secret_access_key=os.environ['AWS_SECRET_ACCESS_KEY']
    )
    s3.put_object(
        Bucket='files',
        Key=file_key,
        Body=docx_bytes,
        ContentType='application/vnd.openxmlformats-officedocument.wordprocessingml.document'
    )

    cdn_url = f"https://cdn.poehali.dev/projects/{os.environ['AWS_ACCESS_KEY_ID']}/bucket/{file_key}"

    return {
        'statusCode': 200,
        'headers': {
            'Access-Control-Allow-Origin': '*',
            'Content-Type': 'application/json'
        },
        'body': json.dumps({'url': cdn_url, 'filename': f'Договор_{data.get("contract_number","")}.docx'})
    }
