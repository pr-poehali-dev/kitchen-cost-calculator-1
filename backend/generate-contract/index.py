def num_to_words_ru(n):
    """Перевод числа в рубли прописью (упрощённый вариант)."""
    try:
        n = int(round(float(n)))
    except Exception:
        return str(n) + ' рублей'

    ones = ['', 'один', 'два', 'три', 'четыре', 'пять', 'шесть', 'семь', 'восемь', 'девять',
            'десять', 'одиннадцать', 'двенадцать', 'тринадцать', 'четырнадцать', 'пятнадцать',
            'шестнадцать', 'семнадцать', 'восемнадцать', 'девятнадцать']
    ones_f = ['', 'одна', 'две', 'три', 'четыре', 'пять', 'шесть', 'семь', 'восемь', 'девять',
              'десять', 'одиннадцать', 'двенадцать', 'тринадцать', 'четырнадцать', 'пятнадцать',
              'шестнадцать', 'семнадцать', 'восемнадцать', 'девятнадцать']
    tens = ['', 'десять', 'двадцать', 'тридцать', 'сорок', 'пятьдесят', 'шестьдесят', 'семьдесят', 'восемьдесят', 'девяносто']
    hundreds = ['', 'сто', 'двести', 'триста', 'четыреста', 'пятьсот', 'шестьсот', 'семьсот', 'восемьсот', 'девятьсот']

    def _three(num, feminine=False):
        h = num // 100
        t = (num % 100) // 10
        o = num % 10
        parts = []
        if h: parts.append(hundreds[h])
        if t == 1:
            parts.append(ones[10 + o])
        else:
            if t: parts.append(tens[t])
            if o:
                parts.append(ones_f[o] if feminine else ones[o])
        return ' '.join(parts)

    def _rub(n):
        last2 = n % 100
        last1 = n % 10
        if 11 <= last2 <= 19:
            return 'рублей'
        elif last1 == 1:
            return 'рубль'
        elif 2 <= last1 <= 4:
            return 'рубля'
        else:
            return 'рублей'

    if n == 0:
        return 'ноль рублей'

    parts = []
    billions = n // 1_000_000_000
    millions = (n % 1_000_000_000) // 1_000_000
    thousands = (n % 1_000_000) // 1_000
    remainder = n % 1_000

    if billions:
        w = _three(billions)
        l = billions % 10
        l2 = billions % 100
        if 11 <= l2 <= 19: s = 'миллиардов'
        elif l == 1: s = 'миллиард'
        elif 2 <= l <= 4: s = 'миллиарда'
        else: s = 'миллиардов'
        parts.append(w + ' ' + s)

    if millions:
        w = _three(millions)
        l = millions % 10
        l2 = millions % 100
        if 11 <= l2 <= 19: s = 'миллионов'
        elif l == 1: s = 'миллион'
        elif 2 <= l <= 4: s = 'миллиона'
        else: s = 'миллионов'
        parts.append(w + ' ' + s)

    if thousands:
        w = _three(thousands, feminine=True)
        l = thousands % 10
        l2 = thousands % 100
        if 11 <= l2 <= 19: s = 'тысяч'
        elif l == 1: s = 'тысяча'
        elif 2 <= l <= 4: s = 'тысячи'
        else: s = 'тысяч'
        parts.append(w + ' ' + s)

    if remainder:
        parts.append(_three(remainder))

    result = ' '.join(p for p in parts if p.strip())
    result = result[0].upper() + result[1:] if result else ''
    result += ' ' + _rub(n)
    return result


def format_date(d):
    if not d:
        return '______'
    try:
        dt = datetime.fromisoformat(d)
        months = ['января','февраля','марта','апреля','мая','июня',
                  'июля','августа','сентября','октября','ноября','декабря']
        return f'{dt.day} {months[dt.month-1]} {dt.year} г.'
    except Exception:
        return d


def set_cell_bg(cell, hex_color):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), hex_color)
    tcPr.append(shd)


def add_bold_para(doc, text, size=10, align=WD_ALIGN_PARAGRAPH.CENTER):
    p = doc.add_paragraph()
    p.alignment = align
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(size)
    return p


def add_para(doc, text, size=10, align=WD_ALIGN_PARAGRAPH.JUSTIFY, bold_prefix=None):
    p = doc.add_paragraph()
    p.alignment = align
    if bold_prefix:
        r = p.add_run(bold_prefix)
        r.bold = True
        r.font.size = Pt(size)
    run = p.add_run(text)
    run.font.size = Pt(size)
    return p


